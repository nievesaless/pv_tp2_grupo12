// Declaración de las variables para los lados del rectángulo
let base = 10; 
let altura = 5; 

// Cálculo del área y el perímetro
let area = base * altura;
let perimetro = 2 * (base + altura);

// Mostrar los resultados por consola usando la unidad "cm"
console.log(`El área del rectángulo es: ${area} cm²`);
console.log(`El perímetro del rectángulo es: ${perimetro} cm`);